package entities;

public class Computadora {

	private int idComputadora;
	private String placa_madre;
	private String placa_de_video;
	private String ram;
	private String procesador;
	private String storage;
	private String idTipoComputadora;
	private String estado;
	
	public int getIdComputadora() {
		return idComputadora;
	}
	public void setIdComputadora(int idComputadora) {
		this.idComputadora = idComputadora;
	}
	public String getPlaca_madre() {
		return placa_madre;
	}
	public void setPlaca_madre(String placa_madre) {
		this.placa_madre = placa_madre;
	}
	public String getPlaca_de_video() {
		return placa_de_video;
	}
	public void setPlaca_de_video(String placa_de_video) {
		this.placa_de_video = placa_de_video;
	}
	public String getRam() {
		return ram;
	}
	public void setRam(String ram) {
		this.ram = ram;
	}
	public String getProcesador() {
		return procesador;
	}
	public void setProcesador(String procesador) {
		this.procesador = procesador;
	}
	public String getStorage() {
		return storage;
	}
	public void setStorage(String storage) {
		this.storage = storage;
	}
	public String getIdTipoComputadora() {
		return idTipoComputadora;
	}
	public void setIdTipoComputadora(String idTipoComputadora) {
		this.idTipoComputadora = idTipoComputadora;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	
}
